'use strict'

/**
 * advisoryTemplates — the SINGLE source seam for the Advisor-e template catalogue.
 *
 * ▶ MASTER / HANDOVER TEAM: this is the ONLY file to change to connect the LIVE
 *   Advisory template feed. Today it reads a READ-ONLY JSON snapshot exported from
 *   the master Advisor-e app (design/reference/…). To go live, replace the file
 *   read below with a call to Advisory's template API/DB, keeping the return
 *   shapes of list()/exists() identical — the route (server/routes/templates.js),
 *   the marketplace create-validation (server/routes/people.js), and the frontend
 *   picker then need NO changes.
 *
 * HARD RULE (CLAUDE.md): the snapshot's IDs and content are generated by the
 * master app and must NEVER be edited, renumbered, or generated here. This module
 * only READS it. The `link` field is the authoritative page ID that will drive the
 * Advisory-hosted page URL. Note: the same `link` can appear on more than one
 * catalogue entry — that is master data and is left exactly as-is.
 *
 * Node 14.15 / CommonJS. Loaded and cached once at first use.
 */

const fs = require('fs')
const path = require('path')

// SOURCE SEAM: the read-only snapshot exported from Advisory. When Advisory
// regenerates it, drop the new file in and update this name; or replace the whole
// load with a live Advisory API call (see file header).
const SNAPSHOT = path.join(__dirname, '..', '..', 'design', 'reference', 'search_content_20260625201604.json')

let cache = null

/**
 * Load and cache a slim, safe projection of the catalogue.
 * @returns {Array<{pageId:string,title:string,section:string,subSection:string,tags:string[]}>}
 */
function load () {
  if (cache) { return cache }
  const raw = fs.readFileSync(SNAPSHOT, 'utf8')
  const rows = JSON.parse(raw)
  // Slim projection for the picker — the page ID (`link`), the tool name, where it
  // sits, its `purpose` (used to pre-fill the listing summary), and tags to search
  // on. We never expose the deeper record (cpd/growth/status) or mutate anything.
  cache = rows.map(r => ({
    pageId: r.link,
    title: r.title,
    section: r.section,
    subSection: r.subSection,
    purpose: r.purpose || '',
    tags: Array.isArray(r.tags) ? r.tags : []
  }))
  return cache
}

function matches (t, q) {
  if (!q) { return true }
  const hay = [t.title, t.section, t.subSection].concat(t.tags || []).join(' ').toLowerCase()
  return q.toLowerCase().split(/\s+/).filter(Boolean).every(term => hay.includes(term))
}

/**
 * List templates for the "Choose the Advisor-e tool" picker.
 * Async (like the repository seam) so the live Advisory feed is a drop-in later.
 * @param {string} [q] optional search over title/section/tags
 * @returns {Promise<Array>} slim template rows (see load())
 */
async function list (q) {
  return load().filter(t => matches(t, q))
}

/**
 * Is this page ID a real Advisor-e template? Used to reject invented IDs on create
 * — the frontend must never be trusted (CLAUDE.md security rule).
 * @param {string} pageId
 * @returns {Promise<boolean>}
 */
async function exists (pageId) {
  if (!pageId) { return false }
  return load().some(t => t.pageId === pageId)
}

module.exports = { list, exists }
